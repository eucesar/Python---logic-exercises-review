#Use um loop while para imprimir os números pares de 1 a 10
#while leh <= e etc.

numero = 0 #tem q ficar do lado de fora do while - vale 0 em diante
while numero <= 10: #ate onde vai o maximo do loop
    if numero % 2 == 0: #será par
        print('Par', numero)
    numero += 1 #fica soh 1 item do loop = ent uso numero += pra adicionar os outros elementos 

 
